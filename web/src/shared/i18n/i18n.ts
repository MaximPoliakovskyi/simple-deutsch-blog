export function resolveClientLocale(): Locale {
  if (typeof window !== "undefined") {
    const pathnameLocale = parseLocaleFromPath(window.location.pathname);
    if (pathnameLocale) return pathnameLocale;
  }
  if (typeof document !== "undefined") {
    const documentLocale = document.documentElement.lang;
    if (documentLocale) {
      try {
        return assertLocale(documentLocale);
      } catch {}
    }
  }
  return DEFAULT_LOCALE;
}

const INTL_LOCALE_BY_LOCALE: Record<Locale, string> = { en: "en-US", ru: "ru-RU", uk: "uk-UA" };
const LONG_DATE_FORMAT = { dateStyle: "long" } satisfies Intl.DateTimeFormatOptions;

export function getIntlLocale(locale: Locale): string {
  return INTL_LOCALE_BY_LOCALE[locale];
}

export function formatLocalizedDate(
  value: Date | string | null | undefined,
  locale: Locale,
  options: Intl.DateTimeFormatOptions = LONG_DATE_FORMAT,
): string | null {
  if (!value) return null;
  const date = value instanceof Date ? value : new Date(value);
  if (Number.isNaN(date.getTime())) return null;
  return new Intl.DateTimeFormat(getIntlLocale(locale), options).format(date);
}

export function formatLongDate(
  value: Date | string | null | undefined,
  locale: Locale,
  options: Intl.DateTimeFormatOptions = {},
): string | null {
  return formatLocalizedDate(value, locale, { ...LONG_DATE_FORMAT, ...options });
}

