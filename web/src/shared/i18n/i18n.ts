export function buildLevelMetadataCopy(
  locale: Locale,
  tag: string,
  term: DescribedTerm,
  options: { includeEmoji?: boolean } = {},
) {
  const dictionary = getTranslations(locale);
  const prefix = dictionary["level.titlePrefix"];
  const cefrCode = getCefrCodeFromSlug(tag);
  const levelLabel = getLevelLabel(tag, locale);
  const emoji = options.includeEmoji && cefrCode ? (CEFR_UI_CONFIG[cefrCode]?.emoji ?? "") : "";
  const title =
    cefrCode && levelLabel
      ? `${prefix} ${emoji ? `${emoji} ` : ""}${cefrCode} (${levelLabel})`
      : `${prefix} ${term.name}`;
  return {
    title,
    description:
      term.description ??
      formatTranslation(dictionary["level.meta.description"], { name: term.name }),
  };
}

type SlavicPluralForm = "few" | "many" | "one";
function getSlavicPluralForm(count: number): SlavicPluralForm {
  const mod10 = count % 10;
  const mod100 = count % 100;
  if (mod10 === 1 && mod100 !== 11) return "one";
  if (mod10 >= 2 && mod10 <= 4 && !(mod100 >= 12 && mod100 <= 14)) return "few";
  return "many";
}

export function formatLocalizedPostCount(count: number, locale: Locale) {
  const dictionary = getTranslations(locale);
  if (locale === "en")
    return `${count} ${count === 1 ? dictionary["post.count.one"] : dictionary["post.count.many"]}`;
  const form = getSlavicPluralForm(count);
  return `${count} ${dictionary[`post.count.${form}`]}`;
}

