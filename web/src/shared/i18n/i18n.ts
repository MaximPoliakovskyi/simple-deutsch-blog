function getCefrCodeFromSlug(slug?: string | null): CefrCode | undefined {
  const normalized = String(slug ?? "").toUpperCase();
  return CEFR_CODES.includes(normalized as CefrCode) ? (normalized as CefrCode) : undefined;
}

function getLevelLabel(slug: string, locale: Locale): string | undefined {
  const code = getCefrCodeFromSlug(slug);
  if (!code) return undefined;
  const dictionary = getTranslations(locale);
  return dictionary[`cefr.${code}.title` as TranslationKey];
}

