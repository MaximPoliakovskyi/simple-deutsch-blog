function makeLookupVariants(s: string | undefined | null) {
  if (!s) return [] as string[];
  const raw = String(s).trim().toLowerCase();
  const stripped = raw.replace(/[^\p{L}\p{N}]+/gu, " ").trim();
  return Array.from(
    new Set([
      raw,
      stripped,
      stripped.replace(/\s+/g, "-"),
      raw
        .replace(/&/g, "and")
        .replace(/[^\p{L}\p{N}]+/gu, " ")
        .trim(),
      raw
        .replace(/&/g, "and")
        .replace(/[^\p{L}\p{N}]+/gu, " ")
        .trim()
        .replace(/\s+/g, "-"),
    ]),
  );
}

function extractLeadingSymbols(s: string | undefined | null) {
  if (!s) return "";
  let prefix = "";
  for (const ch of String(s)) {
    if (/\p{L}|\p{N}/u.test(ch)) break;
    prefix += ch;
  }
  return prefix.replace(/\s+$/u, "");
}

export function translateCategory(
  name?: string | null,
  slug?: string | null,
  locale: Locale = "en",
) {
  const safeLocale = locale === "uk" || locale === "ru" ? locale : "en";
  const emojiPrefix = extractLeadingSymbols(name ?? slug ?? "");
  for (const key of makeLookupVariants(slug))
    if (CATEGORY_TRANSLATION_MAP[key]?.[safeLocale])
      return `${emojiPrefix} ${String(CATEGORY_TRANSLATION_MAP[key]?.[safeLocale]).trimStart()}`;
  for (const key of makeLookupVariants(name))
    if (CATEGORY_TRANSLATION_MAP[key]?.[safeLocale])
      return `${emojiPrefix} ${String(CATEGORY_TRANSLATION_MAP[key]?.[safeLocale]).trimStart()}`;
  return emojiPrefix
    ? `${emojiPrefix} ${(name ?? slug ?? "").slice(emojiPrefix.length).trimStart()}`
    : (name ?? slug ?? "");
}

export function translateCategoryDescription(
  original?: string | null,
  slug?: string | null,
  locale: Locale = "en",
) {
  const safeLocale = locale === "uk" || locale === "ru" ? locale : "en";
  for (const key of makeLookupVariants(slug))
    if (CATEGORY_DESC_MAP[key]?.[safeLocale])
      return String(CATEGORY_DESC_MAP[key]?.[safeLocale]).trim();
  for (const key of makeLookupVariants(original))
    if (CATEGORY_DESC_MAP[key]?.[safeLocale])
      return String(CATEGORY_DESC_MAP[key]?.[safeLocale]).trim();
  return undefined;
}
